function flechaI() {
    var elemento = document.getElementById("flechaI"); // obtenemos el elemento por ID
    var canvas = elemento.getContext("2d"); // este metodo genera un contexto de dibujo en lienzo
    canvas.fillStyle="white";
    canvas.fillRect(100, 50, 100, 50); //(x,y, ancho, altura) rectangulo solido
    canvas.fillStyle="white";
    canvas.beginPath();
    canvas.moveTo(100,20);
    canvas.lineTo(100,130);
    canvas.lineTo(0,80);
    canvas.fill();
    canvas.strokeStyle="white";
    canvas.closePath();
    canvas.stroke();
    }
    window.addEventListener("load", flechaI); // para llamar al evento
